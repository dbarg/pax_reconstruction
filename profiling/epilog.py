def main():

    env = get_slurm_env()
    
    with open("/scratch/midway2/dbarge/slurm/tmp.txt", "w") as f:
        
        f.write('Hello')
        
        for k in env:
            f.write("{0} = {1}\n".format(k, env[k]))
    
